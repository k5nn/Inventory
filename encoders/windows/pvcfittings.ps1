# /insert_item/{item}/{unit}/{category}/{amount}/{warning}/{retail}/{wholesale}/{capital}/{formula}

function push_to_db {

    param (

        [Parameter (Mandatory=$true , position=0)]
        [String]$str_notation,
        [Parameter (Mandatory=$true , position=1)]
        [String]$color
    )

    $split_arr = $str_notation.split( "_" )

    if ( $split_arr[0] -eq "12" ) {
        $size = "1/2"
    } elseif ( $split_arr[0] -eq "34" ) {
        $size = "3/4"
    } elseif ( $split_arr[0] -eq "114" ) {
        $size = "1 1/4"
    } elseif ( $split_arr[0] -eq "112" ) {
        $size = "1 1/2"
    } else {
        $size = $split_arr[0]
    }

    $dirty_name = $split_arr[1] + " " + $size + " (" + $color + ")"
    $replace_space = $dirty_name.Replace( " " , "%20" )
    $replace_ffslash = $replace_space.Replace( "/" , "%5D" )
    $replace_singlequote = $replace_ffslash.Replace( "'" , "%27" )
    $replace_pound = $replace_singlequote.Replace( "#" , "%23" )
    $unit = $split_arr[2]
    $category = "PVC%20Fittings"
    $amount = $split_arr[3]
    $warning = $split_arr[4]
    $retail = $split_arr[5]
    $wholesale = $split_arr[6]
    $capital = $split_arr[7]
    $formula = $split_arr[8].Replace( "+" , "p" )

    $uri = "http://localhost:8080/insert_item/" + $replace_pound + "/" + $unit + "/" + $category + "/" + $amount + "/" + $warning + "/" + $retail + "/" + $wholesale + "/" + $capital + "/" + $formula

    $uri
    
    # Invoke-WebRequest -uri $uri -Method 'PUT'
}

